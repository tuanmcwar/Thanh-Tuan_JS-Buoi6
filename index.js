function inDiv() {
  var soThe = 10;
  var soDem = 1;
  var html = "";
  while (soDem <= soThe) {
    if (soDem % 2 === 0) {
      var IntheDIv =
        "<div style='background: red;line-height:40px;padding-left: 20px;color: #fff;'> Div chản </div>";

      html += IntheDIv;
    } else {
      var IntheDIv =
        "<div style='background: blue;line-height:40px;padding-left: 20px;color: #fff; '> Div lẻ </div>";
      html += IntheDIv;
    }
    soDem++;
  }
  document.getElementById("txtB6").innerHTML = html;
}
document.getElementById("btnB6").onclick = function () {
  inDiv();
};

function isNt(n) {
  var i = 2;
  while (i <= Math.sqrt(n)) {
    if (n % i == 0) {
      return false;
    }
    i++;
  }
  return true;
}

function DemSNT() {
  var soNT = Number(document.getElementById("num1B7").value);
  var string = "";
  var n = 2;
  while (n <= soNT) {
    if (isNt(n)) {
      string += n + " ";
    }
    n++;
  }
  document.getElementById("txtB7").innerHTML = string;
}

document.getElementById("btnB7").onclick = function () {
  DemSNT();
};
