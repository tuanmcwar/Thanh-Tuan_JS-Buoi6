function Xuatsochiahetcho3() {
  var so1 = 100;
  var n1 = 0;
  var chuoi1 = "";
  var chuoi2 = "";

  while (n1 < so1) {
    if (n1 % 2 === 0) {
      chuoi1 += n1 + " ";
    } else {
      chuoi2 += n1 + " ";
    }
    n1++;
  }
  document.getElementById("txtB1").innerHTML =
    "Số chẳn: " + chuoi1 + "</br>" + " 👉Số lẻ: " + chuoi2;
}
document.getElementById("btnB1").onclick = function () {
  Xuatsochiahetcho3();
};
